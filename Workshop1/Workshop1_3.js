let math = 85;
let science = 72;
let english = 90;

// คำนวณค่าเฉลี่ยและคะแนนรวม
let total = math + science + english;
let average = total / 3;

console.log("คะแนนรวม:", total);
console.log("ค่าเฉลี่ย:", average);

// เปรียบเทียบค่าเฉลี่ย
console.log("ค่าเฉลี่ย >= 80 หรือไม่:", average >= 80);